package myFirstProgram;

public class MyHelloWorldGreetings {
	
	/**
	 * Java Convention/Rules
	 * 1) Java project must start with Capital letter
	 * 2) folders (e.g packages) must start with small letter
	 * 3) Class name must start with Capital
	 * 4) methods must start with small letter
	 * 5) java is case sensitive i.e small letter is not the same as capital letter
	 *     main != Main
	 * 
	 */
	
	public static void main(String[] args) {
		System.out.println("Hello World!!");
		
		System.out.println("I love Java!!");
		
		System.out.print("I love Java a lot!! \n");
		
		System.out.print("I love Java a lot!!");
	}

}
